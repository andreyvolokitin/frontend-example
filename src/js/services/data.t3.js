import App from '../app';

/**
 * General data storage.
 */
App.addService('data', () => {
  //-----------------------------------------------------------
  // Private
  //-----------------------------------------------------------

  const data = {};

  //-----------------------------------------------------------
  // Public
  //-----------------------------------------------------------

  return {
    set(key, value) {
      data[key] = value;
    },
    get(key) {
      return data[key];
    },
  };
});
